static uint32_t keypress_time;

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  keypress_time = timer_read32();
  switch (keycode) {
    case COLEMAK:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_COLEMAK);
      }
      return false;
      break;
    case GAME:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_GAME);
      }
      return false;
      break;
  }
  return true;
}

bool get_retro_tapping(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case LWR_BCK:
        case RSE_ENT:
        case ADJ_DEL:
        case NAV_SPC:
        case MSE_ESC:
        case NHD_TAB:
            return true;
        default:
            return false;
    }
}


